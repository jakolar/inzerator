"""Heightfield pipeline: per-location LERC heightmaps + WebP/JPEG orthos.

Replaces the GLB mesh pipeline (outer/closeup/inner.glb, ~165 MB/loc) with
a 2-ring heightfield (closeup 3km/1.5m + inner 1km/0.5m, ~10-18 MB/loc).

Reads existing `tiles_v2_<slug>/manifest.json` for the location centre, then
writes outputs to `tiles_v2_<slug>/heightfield/` (LERC heightmaps + bare-
earth + 3-tier ortho in WebP+JPEG + cadastre overlay + sub-manifest).

Run:
    python3 gen_heightfield.py --slug hrensko
    python3 gen_heightfield.py --cx -624843 --cy -1095334 --out some/dir
"""
